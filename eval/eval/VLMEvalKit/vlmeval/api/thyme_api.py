def ensure_image_url(image: str) -> str:
    prefixes = ['http://', 'https://', 'file://', 'data:image;']
    if any(image.startswith(prefix) for prefix in prefixes):
        return image
    if os.path.exists(image):
        return image # GPT4V wrapper handles local paths by encoding them
    raise ValueError(f'Invalid image: {image}')

def ensure_temp_img_dir(temp_img_dir: str) -> str:
    """
    Ensure the temporary image directory exists and is writable.
    This directory will be used to save all intermediate images generated by the sandbox.
    
    Args:
        temp_img_dir: Path to the temporary image directory
        
    Returns:
        Absolute path to the temporary image directory
    """
    temp_path = Path(temp_img_dir)
    temp_path.mkdir(parents=True, exist_ok=True)
    
    # Check if directory is writable
    if not os.access(temp_path, os.W_OK):
        raise PermissionError(f"Temporary image directory {temp_img_dir} is not writable.")
    
    return str(temp_path)

class ThymeAPI(ThymePromptMixin, GPT4V):
    def __init__(self, 
                 max_iterations=5, 
                 max_retry=5, 
                 use_custom_prompt=True, 
                 post_process=True,
                 verbose=False,
                 save_conversations=False,
                 conversations_dir=None,
                 temp_img_dir=None,
                 **kwargs):
        self.max_iterations = max_iterations
        self.max_retry = max_retry
        self.post_process = post_process
        self.verbose = verbose
        self.save_conversations = save_conversations
        self.conversations_dir = conversations_dir
        self.temp_img_dir = temp_img_dir
        self.logger = logging.getLogger('ThymeAPI')
        
        super().__init__(use_custom_prompt=use_custom_prompt, verbose=verbose, **kwargs)

    def _extract_image_path(self, inputs):
        # inputs is a list of dicts [{'type': '...', 'value': '...'}]
        for item in inputs:
            if item['type'] == 'image':
                return item['value']
        return ""

    def _prepare_content(self, inputs):
        # Similar to Thyme model but adapted for API inputs
        user_image_path = self._extract_image_path(inputs)
        content = []
        for s in inputs:
            if s['type'] == 'image':
                # GPT4V wrapper expects 'value' for image path
                content.append({'type': 'image', 'value': s['value']})
            elif s['type'] == 'text':
                text = s['value']
                if self.use_custom_prompt:
                    text = generate_prompt_final_qa(text, user_image_path)
                content.append({'type': 'text', 'value': text})
        return content

    def _prepare_content_simple(self, inputs):
        content = []
        for s in inputs:
            if s['type'] == 'image':
                content.append({'type': 'image', 'value': s['value']})
            elif s['type'] == 'text':
                text = s['value']
                if self.use_custom_prompt:
                    text = generate_prompt_simple_qa(text)
                content.append({'type': 'text', 'value': text})
        return content

    def _extract_box_answer(self, response):
        resp = response.split('\\boxed{')[-1]
        lt = len(resp)
        counter, end = 1, None
        for i in range(lt):
            if resp[i] == '{':
                counter += 1
            elif resp[i] == '}':
                counter -= 1
            if counter == 0:
                end = i
                break
            elif i == lt - 1:
                end = lt
                break
        if end is not None:
            response = resp[:end]
        return response

    def prepare_inputs(self, inputs):
        """
        Override GPT4V (OpenAIWrapper) prepare_inputs to allow assistant as the last role.
        Also ensures assistant messages have string content (not list).
        """
        input_msgs = []
        if self.system_prompt is not None:
            input_msgs.append(dict(role='system', content=self.system_prompt))
        assert isinstance(inputs, list) and isinstance(inputs[0], dict)
        assert np.all(['type' in x for x in inputs]) or np.all(['role' in x for x in inputs]), inputs
        
        if 'role' in inputs[0]:
            # Inputs is already a list of messages
            for item in inputs:
                role = item['role']
                content = item['content']
                
                if role == 'assistant':
                    # Ensure content is string for assistant
                    if isinstance(content, list):
                        text_content = ""
                        for c in content:
                            if c.get('type') == 'text':
                                text_content += c.get('value', '') or c.get('text', '')
                        input_msgs.append(dict(role=role, content=text_content))
                    else:
                        input_msgs.append(dict(role=role, content=content))
                else:
                    # For user messages, use prepare_itlist to handle images
                    input_msgs.append(dict(role=role, content=self.prepare_itlist(content)))
        else:
            # Inputs is a list of content items for a single user message
            input_msgs.append(dict(role='user', content=self.prepare_itlist(inputs)))
            
        return input_msgs

    def call_api_direct(self, messages, **kwargs):
        """
        Directly call the API using requests, bypassing OpenAIWrapper.generate_inner
        to avoid assertion errors and handle custom logic.
        """
        temperature = kwargs.pop('temperature', self.temperature)
        max_tokens = kwargs.pop('max_tokens', self.max_tokens)

        # Get next key if using key rotator
        current_key = self.key
        if self.key_rotator is not None:
            current_key = self.key_rotator.get_next_key()
            if self.verbose:
                stats = self.key_rotator.get_stats()

        # Prepare headers
        if self.use_azure:
            headers = {'Content-Type': 'application/json', 'api-key': current_key}
        elif 'internvl2-pro' in self.model:
            headers = {'Content-Type': 'application/json', 'Authorization': current_key}
        else:
            headers = {'Content-Type': 'application/json', 'Authorization': f'Bearer {current_key}'}
        if hasattr(self, 'baidu_appid'):
            headers['appid'] = self.baidu_appid

        # Prepare payload
        # 输出一个 messages 的结构，不需要内容
        
        payload = dict(
            model=self.model,
            messages=messages, # messages are already prepared by prepare_inputs
            n=1,
            temperature=temperature,
            **kwargs)

        if self.o1_model:
            payload['max_completion_tokens'] = max_tokens
            payload.pop('temperature', None)
        else:
            payload['max_tokens'] = max_tokens

        if 'gemini' in self.model:
            # payload.pop('max_tokens', None)
            # payload.pop('n', None)
            if 'gemini-2.5' in self.model:
                payload['reasoning_effort'] = 'high'
            else:
                payload['extra_body']={
                        'google':{
                            'thinking_config': {
                                'thinking_level': 'high'
                            }
                        }
                    }
            
            # Check if there are multiple images in the messages
            image_count = 0
            for msg in messages:
                if isinstance(msg.get('content'), list):
                    for item in msg['content']:
                        if isinstance(item, dict):
                            if item.get('type') == 'image':
                                image_count += 1
                            elif item.get('type') == 'image_url':
                                image_count += 1
            
            # HIGH media resolution only supports single images, use LOW for multiple images
            # Update detail field in all image_url objects
            if image_count > 1:
                for msg in messages:
                    if isinstance(msg.get('content'), list):
                        for item in msg['content']:
                            if isinstance(item, dict) and item.get('type') == 'image_url':
                                if 'image_url' in item and isinstance(item['image_url'], dict):
                                    item['image_url']['detail'] = 'low'

        
        # Handle stop parameter
        if 'stop' in payload and not payload['stop']:
            payload.pop('stop')
        try:
            # print("payload:", json.dumps(payload))
            # print("Model name", self.model)
            response = requests.post(
                self.api_base,
                headers=headers, data=json.dumps(payload), timeout=self.timeout * 1.1)


            ret_code = response.status_code
            ret_code = 0 if (200 <= int(ret_code) < 300) else ret_code
            
            if ret_code == 0:
                resp_struct = json.loads(response.text)
                answer = resp_struct['choices'][0]['message']['content'].strip()
                if answer is None:
                    answer = resp_struct['choices'][0]['message']['reasoning_content'].strip()
                    
                # remove </think> content in answer
                # print('BEFORE REMOVE answer\n', answer)
                # print("Model name", self.model)
                if 'thinking' in self.model or 'Thinking' in self.model:
                    answer = answer.split('</think>')
                    # assert len(_item_text) == 2
                    answer = answer[-1]
                # print('AFTER answer\n', answer)
                return 0, answer, response
            else:
                return ret_code, response.text, response
                
        except Exception as err:
            self.logger.error(f'{type(err)}: {err}')
            if 'response' in locals() and hasattr(response, 'text'):
                self.logger.error(response.text)
            return -1, str(err), None

    def generate_inner(self, inputs, sample_id=None, dataset_name=None, **kwargs):
        # inputs is a list of content items: [{'type': 'text', 'value': '...'}, {'type': 'image', 'value': '...'}]
        
        user_image_path = self._extract_image_path(inputs)
        
        # Initialize conversation saver
        conv_saver = None
        if self.save_conversations:
            default_log_dir = os.path.join(
                os.path.dirname(os.path.dirname(os.path.dirname(__file__))),
                'logs'
            )
            conv_saver = create_conversation_saver(
                work_dir=self.conversations_dir or default_log_dir,
                model_name='ThymeAPI',
                dataset_name=dataset_name or 'unknown',
                sample_id=sample_id,
                image_path=user_image_path,
                verbose=self.verbose
            )
            temp_output_dir = str(conv_saver.images_dir)
        elif self.temp_img_dir:
            # Use specified temporary image directory
            temp_output_dir = ensure_temp_img_dir(self.temp_img_dir)
        else:
            import tempfile
            temp_output_dir = tempfile.mkdtemp(prefix='thyme_sandbox_')
        
        if self.verbose:
            print(f"Intermediate images will be saved to: {temp_output_dir}")
        
        self.system_prompt = REASONING_SYS_PROMPT
        
        # Initial messages
        messages = []
        messages.append({'role': 'user', 'content': self._prepare_content(inputs)})
        
        if conv_saver:
            user_text = ""
            for item in inputs:
                if item['type'] == 'text':
                    user_text = item['value']
                    break
            image_paths = [item['value'] for item in inputs if item['type'] == 'image']
            conv_saver.add_user_message(user_text, image_paths)

        retry_generations = self.max_retry
        has_valid_answer = False
        final_answer = ""
        
        previous_execution_context = {}
        all_executed_code_blocks = []
        all_generated_images = []

        while (retry_generations > 0) and (not has_valid_answer):
            conversation_history = copy.deepcopy(messages)
            retry_iterations = self.max_iterations
            generation_attempt = self.max_retry - retry_generations + 1
            
            if self.verbose:
                print(f'\033[32m\n--- Generation {generation_attempt} ---\033[0m')

            while retry_iterations > 0:
                retry_iterations -= 1
                iteration_num = self.max_iterations - retry_iterations
                
                if conv_saver:
                    conv_saver.add_iteration(iteration_num)
                
                # Prepare inputs for API
                api_messages = self.prepare_inputs(conversation_history)
                
                api_kwargs = dict(kwargs)
                if 'stop' not in api_kwargs:
                    api_kwargs['stop'] = SPECIAL_STRING_LIST
                
                # Call API directly
                ret_code, answer, response = self.call_api_direct(api_messages, **api_kwargs)
                
                if conv_saver:
                    conv_saver.add_api_call(conversation_history, answer, iteration_num)
                
                if ret_code != 0:
                    print(f"API Error: {answer}")
                    if conv_saver:
                        conv_saver.add_generation_attempt(generation_attempt, False, f"API Error: {answer}")
                    break 

                generated_text_segment = answer
                
                # Append assistant response (code/text) to history
                # Note: We append it as a simple text content for assistant, which prepare_inputs will handle
                conversation_history.append({'role': 'assistant', 'content': [{'type': 'text', 'value': generated_text_segment}]})
                
                # Check for answer
                has_complete_answer = "</answer>" in generated_text_segment or "<|answer|>" in generated_text_segment
                has_incomplete_answer = False
                
                if not has_complete_answer:
                    if "<answer>" in generated_text_segment:
                        has_incomplete_answer = True
                        if self.verbose:
                            print(f"\033[33m--- Incomplete Answer tag detected ---\033[0m")
                    if generated_text_segment.count("<|answer|>") > 2:
                        has_incomplete_answer = True
                        if self.verbose:
                            print(f"\033[33m--- Incomplete Answer tag detected ---\033[0m")
                
                if has_complete_answer or has_incomplete_answer:
                    if has_incomplete_answer and "</answer>" not in generated_text_segment and "<|answer|>" not in generated_text_segment:
                        generated_text_segment += "</answer>"
                        # Update the last message in history with the completed tag
                        conversation_history[-1]['content'][0]['value'] = generated_text_segment
                    
                    has_valid_answer = True
                    if self.verbose:
                        print("\033[32m--- Final answer tag found. ---\033[0m")
                    
                    if conv_saver:
                        conv_saver.add_assistant_message(
                            generated_text_segment,
                            all_executed_code_blocks if all_executed_code_blocks else None,
                            all_generated_images if all_generated_images else None
                        )
                        conv_saver.add_generation_attempt(generation_attempt, True)
                    break

                # Check for code
                code_regex = re.compile(r'<code>\s*(?:```\s*)?(?:python\s*)?([\s\S]*?)\s*(?:```\s*)?</code>', re.IGNORECASE)
                code_match = code_regex.search(generated_text_segment)
                
                if not code_match:
                    # Try to extract incomplete code block (missing </code>)
                    # Look for <code> followed by optional ``` markers and code content
                    # We need to be careful to extract only the actual code block, not explanatory text
                    incomplete_code_regex = re.compile(
                        r'<code>\s*```(?:python|py)?\s*\n(.*?)(?:```|$)',
                        re.DOTALL | re.IGNORECASE
                    )
                    incomplete_match = incomplete_code_regex.search(generated_text_segment)
                    
                    if incomplete_match:
                        code_match = incomplete_match
                        if self.verbose:
                            print(f"\033[33m--- Incomplete Code Block detected (missing end marker) ---\033[0m")
                    else:
                        # Fallback: try without ``` markers (bare code after <code>)
                        # Match until we hit common markdown patterns or end of string
                        bare_code_regex = re.compile(
                            r'<code>\s*\n((?:(?!###|\*\*|<sandbox_output>)[\s\S])*)',
                            re.IGNORECASE
                        )
                        bare_match = bare_code_regex.search(generated_text_segment)
                        if bare_match:
                            code_match = bare_match
                            if self.verbose:
                                print(f"\033[33m--- Incomplete Code Block detected (bare code) ---\033[0m")

                if code_match:
                    code_to_execute = code_match.group(1).strip()
                    
                    # Use the improved clean_code_string from sandbox module
                    from ..vlm.thyme.sandbox import clean_code_string
                    code_to_execute = clean_code_string(code_to_execute)
                    if self.verbose:
                        print(f"\033[31m--- Found Code Block ---\n{generated_text_segment}\n-------------------------\033[0m")
                    
                    # Track this code block
                    all_executed_code_blocks.append(code_to_execute)
                    
                    processed_img_paths, captured_stdout, error_msg, current_execution_context = execute_code_in_sandbox(
                        code_to_execute, user_image_path, temp_output_dir=temp_output_dir,
                        previous_execution_context=previous_execution_context
                    )
                    previous_execution_context = current_execution_context
                    
                    # Track generated images
                    if processed_img_paths:
                        all_generated_images.extend(processed_img_paths)
                    
                    if not processed_img_paths:
                        print(f'{error_msg}')
                        # break
                        # 把错误信息告诉大模型,让它在下一轮迭代中尝试不同的方法
                        conversation_history.append({
                            'role': 'user',
                            'content': [{'type': 'text', 'value': f"Error: {error_msg}. Please try a different approach."}]
                        })
                        # 不要break,继续下一次迭代让模型看到错误信息
                        continue

                    # Build sandbox output content
                    sandbox_content = [
                        {'type': 'text', 'value': generated_text_segment},
                        {'type': 'text', 'value': "<sandbox_output>"}
                    ]
                    
                    # Get basenames of processed images to identify moved files
                    processed_img_basenames = {os.path.basename(p) for p in processed_img_paths}
                    
                    # Process captured stdout: parse text and extract image paths
                    if captured_stdout:
                        stdout_lines = captured_stdout.strip().split('\n')
                        for line in stdout_lines:
                            line = line.strip()
                            if not line:
                                continue
                            
                            # Try to find image paths within the line
                            # Pattern: match file paths with image extensions
                            image_path_pattern = r'([/\w\-_.]+\.(?:jpg|jpeg|png|gif|bmp|webp|tiff))'
                            matches = list(re.finditer(image_path_pattern, line, re.IGNORECASE))
                            
                            if matches:
                                # Check if any match is an actual existing file
                                valid_paths = []
                                for match in matches:
                                    potential_path = match.group(1)
                                    # Check if this is a file that was moved (compare basename)
                                    basename = os.path.basename(potential_path)
                                    if basename in processed_img_basenames:
                                        # This file was moved, skip it in stdout (will be added from processed_img_paths)
                                        continue
                                    if os.path.isfile(potential_path):
                                        valid_paths.append((match.start(), match.end(), potential_path))
                                
                                if valid_paths:
                                    # Split line into text and image parts
                                    last_end = 0
                                    for start, end, img_path in valid_paths:
                                        # Add text before image path
                                        text_before = line[last_end:start].strip()
                                        if text_before:
                                            sandbox_content.append({'type': 'text', 'value': text_before})
                                        # Add image
                                        sandbox_content.append({'type': 'image', 'value': img_path})
                                        last_end = end
                                    
                                    # Add remaining text after last image path
                                    text_after = line[last_end:].strip()
                                    if text_after:
                                        sandbox_content.append({'type': 'text', 'value': text_after})
                                else:
                                    # Check if the entire line is just a moved image path
                                    line_basename = os.path.basename(line)
                                    if line_basename not in processed_img_basenames:
                                        # No valid image paths found, treat as text
                                        sandbox_content.append({'type': 'text', 'value': line})
                                    # else: skip this line as it's just a moved image path
                            else:
                                # No image path pattern found, treat as regular text
                                sandbox_content.append({'type': 'text', 'value': line})
                    
                    # Add processed image paths (from return value)
                    for img_path in processed_img_paths:
                        if os.path.exists(img_path):
                            # Check if this path is not already in sandbox_content (from stdout)
                            already_added = any(
                                item.get('type') == 'image' and item.get('value') == img_path 
                                for item in sandbox_content
                            )
                            if not already_added:
                                sandbox_content.append({'type': 'image', 'value': img_path})
                    
                    sandbox_content.append({'type': 'text', 'value': "The returning status and the processed image or text (if any) of the code is attached, you can continue to write code to manipulate the image with different tools or options for better understanding or proceed to answer the question if you have meet the correct option."})
                    sandbox_content.append({'type': 'text', 'value': "</sandbox_output>"})
                    
                    # Append sandbox output as USER message
                    conversation_history.append({'role': 'user', 'content': sandbox_content})
                    
                else:
                    # No code. No </answer>, assume repetition generated, break.
                    if "</answer>" not in generated_text_segment:
                        print('No code. No </answer>')
                        print(generated_text_segment)
                        break 

            if has_valid_answer:
                break
            
            retry_generations -= 1
            print('Fail to find a valid answer, retrying...')
            if conv_saver:
                conv_saver.add_generation_attempt(generation_attempt, False, "No valid answer found")

        # Fallback to simple prompt if failed
        if not has_valid_answer:
            if self.verbose:
                print(f"\033[32m\n --- Fail to find a valid answer after {self.max_retry} retrys. Falling back to simple prompt.---\033[0m")
            
            self.system_prompt = SIMPLE_SYS_PROMPT
            
            messages = []
            messages.append({'role': 'user', 'content': self._prepare_content_simple(inputs)})
            
            fallback_kwargs = dict(kwargs)
            if 'stop' not in fallback_kwargs:
                fallback_kwargs['stop'] = ["</answer>"]
            
            # Use direct API call for fallback too
            api_messages = self.prepare_inputs(messages)
            ret_code, answer, response = self.call_api_direct(api_messages, **fallback_kwargs)
            
            if ret_code == 0:
                if "<answer>" not in answer:
                    answer = f"<answer>{answer}</answer>"
                if self.verbose:
                    print(f"\033[32m--- Fallback response: ---\n{answer}\n-------------------------\033[0m")
                conversation_history = messages
                conversation_history.append({'role': 'assistant', 'content': [{'type': 'text', 'value': answer}]})
                has_valid_answer = True
            else:
                print(f"Fallback API Error: {answer}")

        # Extract final answer
        final_assistant_response = ""
        # Find the last assistant message
        for msg in reversed(conversation_history):
            if msg['role'] == 'assistant':
                for item in msg['content']:
                    if item['type'] == 'text':
                        final_assistant_response += item['value']
                break
        
        if conv_saver:
            conv_summary = conv_saver.finalize()
            if self.verbose:
                print(f"Conversation saved to: {conv_summary['sample_dir']}")
        
        if self.post_process:
            if self.verbose:
                print(f"\033[31m--- Final response ---\n{final_assistant_response}\n-------------------------\033[0m")
            
            # Try <answer> tags first
            answer_match = re.search(r"<answer>(.*?)</answer>", final_assistant_response, re.DOTALL)
            if answer_match:
                final_answer = answer_match.group(1).strip()
            else:
                # Try <|answer|> tags
                answer_alt_match = re.search(r"<\|answer\|>(.*?)<\|/answer\|>", final_assistant_response, re.DOTALL)
                if answer_alt_match:
                    final_answer = answer_alt_match.group(1).strip()
                else:
                    answer_alt_match2 = re.search(r"<\|answer\|>(.*?)<\|answer\|>", final_assistant_response, re.DOTALL)
                    if answer_alt_match2:
                        final_answer = answer_alt_match.group(1).strip()
                    else:
                        final_answer = final_assistant_response
            
            match = re.search(r'\\boxed\{(.*?)\}', final_answer)
            if match:
                final_answer = self._extract_box_answer(final_answer)
            
            return 0, final_answer, "Success"
        else:
            return 0, final_assistant_response, "Success"